SELECT

	TF.CODFEC,

	CAST(CL.CODCLIFOR AS VARCHAR) + ' - ' + CL.RAZSOC AS PROPRIETÁRIO,

	RM.NOMMOT,

	TC.CODVEI,

	TF.TOTFEC,

	TF.TOTCRE,

	TF.TOTDEB,

	TC.DEBCRE,

	TC.DATDOC,

	TC.NUMDOC,

	TC.TIPDOC,

	TC.VALOR

FROM

	RODCLI CL INNER JOIN TERFEC TF ON

		CL.CODCLIFOR = TF.CODCLIFOR

	LEFT JOIN TERCAL TC ON

		TC.CODFEC = TF.CODFEC AND

		TC.CODCLIFOR = TF.CODCLIFOR

	LEFT JOIN RODCON RC ON

		RC.CODCON = TC.NUMDOC AND

		RC.CODFIL = TC.FILDOC

	LEFT JOIN RODMOT RM ON

		TC.CODMOT = RM.CODMOT

WHERE TF.CODFEC = 2233